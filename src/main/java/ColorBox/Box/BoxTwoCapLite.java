package ColorBox.Box;

public class BoxTwoCapLite {

    private int x;
    private int y;
    private int z;
    private int g;

    public BoxTwoCapLite(int x, int y, int z, int g){
        this.x=x;
        this.y=y;
        this.z=z;
        this.g=g;
    }
    public int getX() {
        return x;
    }
    public int getY() {
        return y;
    }
    public int getZ() {
        return z;
    }
    public int getG() {
        return g;
    }
}
