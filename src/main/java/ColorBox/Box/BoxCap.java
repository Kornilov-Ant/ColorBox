package ColorBox.Box;

public class BoxCap {

    private int x;
    private int y;
    private int z;
    private int f;


    public BoxCap(int x, int y, int z, int f) {
        this.x = x;
        this.y = y;
        this.z = z;
        this.f = f;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public int getZ() {
        return z;
    }

    public int getF() {
        return f;
    }
}
