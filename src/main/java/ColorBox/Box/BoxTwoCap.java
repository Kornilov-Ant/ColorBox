package ColorBox.Box;

public class BoxTwoCap {

    private int x;
    private int y;
    private int z;
    private int f;
    private int g;

    public BoxTwoCap(int x, int y, int z, int f, int g){
        this.x=x;
        this.y=y;
        this.z=z;
        this.f=f;
        this.g=g;
    }
    public int getX() {
        return x;
    }
    public int getY() {
        return y;
    }
    public int getZ() {
        return z;
    }
    public int getF() {
        return f;
    }
    public int getG() {
        return g;
    }
}
