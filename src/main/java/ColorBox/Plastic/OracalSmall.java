package ColorBox.Plastic;

public class OracalSmall {
    private final int x = 1000;
    private double y = 0.0;
    private final double z = 500000.0;

    public OracalSmall() {
    }

    public void setY(double y) {
        this.y = y;
    }

    public double getY() {
        return y;
    }

    public int getX() {
        return x;
    }

    public double getZ() {
        return z;
    }
}
